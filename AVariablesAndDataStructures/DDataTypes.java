// Java, c++, c are typed languages as we have to define what kind of data we are storing in the program
// Java have Primitive data types (byte 1byte, short 2bytes, char 2bytes, boolean 1byte, int 4bytes, long 8bytes, float 4bytes, double 8bytes)
// Java have Non-Primitive data types (String, Array, Class, Object, Interface)

public class DDataTypes {
    public static void main(String[] args){
        byte b = 8;
        System.out.println(b);
        char ch = 'a';
        System.out.println(ch);
        boolean var = true;
        System.out.println(var);
        float price = (float)10.5;
        System.out.println(price);


    }
}
