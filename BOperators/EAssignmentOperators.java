// Assignment Operators : =, +=, -=, *=, /=, %=

public class EAssignmentOperators {

    public static void main(String[] args){
        int a = 10;
        int b = 20;
        System.out.println(a+=10); // a = a+10
        System.out.println(b-=15); // b = b-15
        System.out.println(b*=20); //b = b*20
        System.out.println(b /=5); // b = b/5
        System.out.println(b %=5); // b = b%5

    }
    
}
