// There are multiple types of operators in the java
/*
1. Arithmetic operators
2. Relational operators
3. Logical operators
4. Bitwise operators
5. Assignment operators */

// Now let's see Arithmetic Operators binary : +, -, /, %     Unary : ++, --

public class AArithmeticOperators {
    public static void main (String[] args) {

        int a = 10;
        int b = 5;
        //Binary Operators
        System.out.println("Addition is : " + (a+b)); // addition
        System.out.println("Subtraction is : " + (a-b)); // subtraction
        System.out.println("multiplication ia : " + (a*b)); // multiplication
        System.out.println("Division is : " + (a/b)); // division coefficient
        System.out.println("Remainder is : " + (a%b)); // modulo - remainder

    }
}

